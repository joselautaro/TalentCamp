package Modulo1;

public class DatosPrimitivos {
    public static void main(String[] args){
        //Enteros
        //byte
        //Descripción: entero de 8 bits con signo
        byte edad = 25;
        //Byte

        //short
        //Descricion: entero con 16 bits con signo
        short salario = 15000;
        //Shor

        //int
        //Descripción: entero de 32 bits
        int poblacion = 1000000;
        //Integer

        //long
        //Descripcion: entero de 64 bits
        long distanciaEstelar = 87974654646979L;
        //Long

        //float
        //descripcion: numero de punto flotante con precisión simple de 32 bits
        float temperatura = 36.6f;
        //Float

        //double
        //descripcion: numero de punto flotante de precision double de 64 bits
        double precio = 19.99;
        //Double

        //caracter
        //descripcion: caracter unicode de 16 bits
        char iniciar = 'A';
        //Character

        //booleano
        //descripcion: valor logico de verdad o falso
        boolean esMayorEdad = true;
        //Boolean
    }
}
